const contractAddress = "0xd9145CCE52D386f254917e481eB44e9943F39138";

let provider, signer, contract;

async function init() {
  provider = new ethers.providers.Web3Provider(window.ethereum);
  await provider.send('eth_requestAccounts', []);
  signer = provider.getSigner();
  contract = new ethers.Contract(contractAddress, abi, signer);
}

async function registerCharity() {
  const name = document.getElementById('charityName').value;
  const tx = await contract.registerCharity(name);
  await tx.wait();
  alert('Charity registered');
}

async function donate() {
  const addr = document.getElementById('donationAddress').value;
  const amt = document.getElementById('donationAmount').value;
  const tx = await contract.donate(addr, { value: ethers.utils.parseEther(amt) });
  await tx.wait();
  alert('Donation sent');
}

async function createAuction() {
  const start = Math.floor(new Date(document.getElementById('startTime').value).getTime()/1000);
  const end = Math.floor(new Date(document.getElementById('endTime').value).getTime()/1000);
  const tx = await contract.createAuction(start, end);
  await tx.wait();
  alert('Auction created');
}

async function placeBid() {
  const id = document.getElementById('bidAuctionId').value;
  const amt = document.getElementById('bidAmount').value;
  const tx = await contract.bid(id, { value: ethers.utils.parseEther(amt) });
  await tx.wait();
  alert('Bid placed');
}

async function finalizeAuction() {
  const id = document.getElementById('finalizeAuctionId').value;
  const tx = await contract.finalizeAuction(id);
  await tx.wait();
  alert('Auction finalized');
}

window.addEventListener('load', init);